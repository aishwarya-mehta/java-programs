package com.thoughtpearl;

public class Odd {
	
	public static void main(String []args)
	{
		int n = 3;
		if(n == 1)
		{
			System.out.println(n + " is not odd number");
		}
		else
		{
			if(n%2 != 0)
			{
				System.out.println(n + " is odd number");
			}
			else
			{
			System.out.println(n+ " is not odd number");
			}
		}
	}
}
